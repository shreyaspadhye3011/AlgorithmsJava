package mycodes;


public class Fast_Power 
{
    /*
     Pseudo code:
     *  FastPower(a,b) : 

              if b = 1 

                   return a 

              otherwise 

                   c := a*a 

                   ans := FastPower(c,[b/2]) 

               if b is odd 

                   return a*ans 

               otherwise return ans
     end
     * 
     *NOTE:  Here [x] denotes the floor function, that is, the largest integer less than or equal to x. 
     */
}
